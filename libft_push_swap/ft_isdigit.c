#include "libft.h"

int	ft_isdigit(int c, int j)
{
	if ((c == '-' || c == '+') && j == 0)
		return (3);
	if (c == ' ')
		return (2);
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
